//Operator in C language:-
//Assignment Operator(+=,-=,*=,/=,%=)
#include <stdio.h>
int main() {
    int a=5,b=22;
    printf("before assignment\n");
    printf("a=%d\n",a);
    printf("b=%d\n",b);
    a+=15;
    b-=10;
    printf("after assignment\n");
    printf("a=%d\n",a);
    printf("b=%d\n",b);
    return 0;
}
//assignment operator is used to assing value and change the value.
