//Operator in C language:-
//Relational Operator(==,!=,<=,>=,<,>)
#include <stdio.h>
int main() {
    int a=5,b=22;
    printf("%d\n",a>=b);
    printf("%d\n",a<=b);
    printf("%d\n",a<b);
    printf("%d\n",a>b);
    return 0;
}
//relational operator is used to compare the value and result or output is always present as Boolean (0 and 1).
//If condition is true output will 1 and if condition is false output will 0.
