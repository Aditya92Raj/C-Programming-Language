//Basic input & output program.
#include <stdio.h>
int main()
{
    int a,b;
    printf("Enter your first number:");
    scanf("%d",&a);
    printf("Enter your second number:");
    scanf("%d",&b);
    printf("Addition is %d\n",a+b);
    printf("subtraction is %d\n",a-b);
    printf("multiplication is %d\n",a*b);
    return 0;
}
